
public class Descricao {

	private double peso;
	private String tipo;
	private String cor;

	public double GetPeso() {
		return peso;
	}

	public String getTipo() {
		return tipo;
	}

	public String getCor() {
		return cor;
	}

	public void setPeso(double peso) {
		this.peso = peso;
	}

	public void setTipo(String tipo) {
		this.tipo = tipo;
	}

	public void setCor(String cor) {
		this.cor = cor;
	}
}
